#!/bin/bash

echo "开始按照标准化流程批量生成15张高质感广告图片..."
echo "========================================"

# 高质感图片标准化prompt
HIGH_TEXTURE_PROMPT="请参考提供的图片（高质感参考图）的设计风格和排版布局，生成一张高端金融类广告图片。必须严格限制图片尺寸为1600*900。图片中不再需要出现任何人物或吉祥物。

【重要提醒】：
参考图本身的背景是深色/暗调的，但这只是参考图自身的风格——你在生成时绝对不要模仿参考图的暗黑背景！你的任务是在保留排版布局的基础上，创造一个色调与参考图截然不同的全新视觉体验。

【核心保留要求 - 必须1:1照搬】：
1. 必须包含和参考图完全相同的中间标题大字，字号排版一致，一个字都不能错。
2. 左上角的logo必须和参考图保持一致。
3. 右下角的'投资有风险，入市需谨慎'风险提示必须直接从参考图照搬。

【背景重构要求 - 必须彻底差异化，且色调多样化】：
绝对不要照搬参考图中的暗黑色调！请在保留“极具视觉冲击力的高级质感”的前提下，对画面的背景进行完全重构。每次生成都要随机搭配不同的色调和元素：

1. 色调策略（重要——至少50%的生成要选浅奢方向！）：
   - 深奢方向：黑金交织、暗紫与琥珀金、墨蓝与玫瑰金、深咖与铜光
   - 浅奢方向（推荐优先选择！）：暖白与玫瑰金、珍珠白与香槟金、浅灰与铂金银、奶油白与古董金、冰蓝与银白、淡香槟与白金
   浅奢风格的高质感图片同样极具视觉冲击力，不要让所有图片都偏暗。

2. 背景主体多样化（每次从以下方向随机选择，避免重复）：
   - 几何建筑：交错的金色线框结构、层叠的磨砂玻璃透视片、悬浮的渐变几何体块
   - 流体艺术：液态金属球体的流动融合、丝绸波纹的光影折射、金属熔液的动态滴落
   - 光影空间：柔光穿透水晶棱镜的色散效果、发光粒子漂浮场、多层次的景深光晕
   - 材质对比：光面大理石与拉丝金属的碰撞、丝绒织物与玻璃的质感并置、微水泥与黄铜的工业美学
   - 自然灵感：极光流动的色带、日晕穿透薄云、深海光线的折射、沙漠沙丘的光影纹理

3. 光影质感（根据所选色调调整）：
   - 浅奢：使用柔和的侧光和顶光（soft overhead key），营造高级通透感
   - 深奢：使用戏剧化的边缘光（rim light）或Rembrandt光，营造神秘高端感
   - 无论哪种：都必须具备电影级光影渲染、强烈的空间纵深感（前清后虚）、细腻的材质触感

整体画面要极致专业、现代、具有让人眼前一亮的高端质感，同时背景色调必须与参考图截然不同！"

# 生成15张高质感图片
echo "生成15张高质感图片..."
echo "----------------------------------------"

for i in {1..15}; do
    echo "正在生成第 $i 张高质感图片..."

    venv/bin/python3 generate_ad_from_reference.py \
      --reference "./.claude/skills/ad-image-generator/assets/example/高质感" \
      --prompt "$HIGH_TEXTURE_PROMPT" \
      --aspect-ratio "16:9" \
      --config "google_ai_config_gemini.json"

    echo "第 $i 张高质感图片生成完成"
    echo "----------------------------------------"
    sleep 2
done

echo "批量生成完成！"
echo "总共生成: 15张高质感广告图片"
echo "图片保存在: generated_ads/ 目录"
